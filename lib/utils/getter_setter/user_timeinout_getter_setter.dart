class Time {
  static String _date = '';
  static void setDate(String date) {
    _date = date;
  }

  static getDate() {
    return _date;
  }

  static String _timein = '';
  static void setTimeIn(String timein) {
    _timein = timein;
  }

  static getTimeIn() {
    return _timein;
  }

  static String _timeout = '';
  static void setTimeOut(String timeout) {
    _timeout = timeout;
  }

  static getTimeOut() {
    return _timeout;
  }

  static String _total = '';
  static void setTotal(String total) {
    _total = total;
  }

  static getTotal() {
    return _total;
  }
}
