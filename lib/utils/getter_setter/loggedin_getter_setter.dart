class LoggedIn {
  static String _firstname = '';
  static void setFirstName(String firstname) {
    _firstname = firstname;
  }

  static getFirstName() {
    return _firstname;
  }

  static String _lastname = '';
  static void setLastName(String lastname) {
    _lastname = lastname;
  }

  static getLastName() {
    return _lastname;
  }

  static String _email = '';
  static void setEmail(String email) {
    _email = email;
  }

  static getEmail() {
    return _email;
  }

  static String _phone = '';
  static void setPhone(String phone) {
    _phone = phone;
  }

  static getPhone() {
    return _phone;
  }

  static String _password = '';
  static void setPassword(String password) {
    _password = password;
  }

  static getPassword() {
    return _password;
  }

  static String _confirmpassword = '';
  static void setConfirmPassword(String confirmpassword) {
    _confirmpassword = confirmpassword;
  }

  static getConfirmPassword() {
    return _confirmpassword;
  }

  static String _userlevel = '';
  static void setUserLevel(String userlevel) {
    _userlevel = userlevel;
  }

  static getUserLevel() {
    return _userlevel;
  }
}
